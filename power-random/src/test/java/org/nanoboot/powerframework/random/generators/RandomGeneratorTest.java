    @Test
    public void getNext_interval() {
        long min = Integer.MAX_VALUE;
        long max = Integer.MIN_VALUE;
        long from = 36;
        long to = 418;
        for(int i = 0; i < 9999; i++) {
            long randomLong = randomGenerator.next(from, to);
            if(randomLong < from) {
                String msg = "Random number is " + randomLong +", but it should be at least " + from;
                throw new AssertionError(msg);
            }
            if(randomLong > to) {
                String msg = "Random number is " + randomLong +", but it should be at most " + to;
                throw new AssertionError(msg);
            }
            if(randomLong < min) {
                min = randomLong;
            }
            if(randomLong > max) {
                max = randomLong;
            }
        }

        assertEquals(from, min);
        assertEquals(to, max);

    }

