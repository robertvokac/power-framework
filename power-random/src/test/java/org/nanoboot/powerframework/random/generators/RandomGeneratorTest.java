    @Before
    public void setUp() {
        this.randomGenerator = new RandomGenerator() {
            private int currentIndex = 0;
            @Override
            public long next() {
                long toReturn = randomNumbers[currentIndex];
                currentIndex++;
                if(currentIndex > (randomNumbers.length - 1)) {
                    currentIndex = 0;
                    for(int i = 0; i < randomNumbers.length; i++) {
                        randomNumbers[i] = RandomGenerator.getDefaultImplStatic().next();
                    }
                }
                return toReturn;
            }

            @Override
            public RandomGenerator getItself() {
                return this;
            }

            @Override
            public String getName() {
                return "testGenerator";
            }
        };
    }

